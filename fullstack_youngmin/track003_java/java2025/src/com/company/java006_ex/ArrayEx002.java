package com.company.java006_ex;

public class ArrayEx002 {
	public static void main(String[] args) {
		/*
		 * 클래스명 : ArrayEx002 1. 배열명 : arr 2. 값 넣기 : 'A' , 'B' , 'C' , 'D' 3. for +
		 * length 로 출력
		 */

		char[] arr2 = { 'A', 'B', 'C', 'D' };
		for (int a = 0; a < arr2.length; a++) {
			System.out.print(arr2[a] + "\t");
			
			}
		
	}
	

}
